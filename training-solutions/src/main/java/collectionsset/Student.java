package collectionsset;

public class Student {
    private String name;
    private int heightIncm;

    public Student(String name, int heightIncm) {
        this.name = name;
        this.heightIncm = heightIncm;
    }

    public String getName() {
        return name;
    }

    public int getHeightIncm() {
        return heightIncm;
    }
}
